package com.sergio.twitter.tweets.ui;

import android.content.Context;
import android.graphics.Bitmap;
import android.graphics.BitmapFactory;
import android.graphics.drawable.Drawable;
import android.support.annotation.Nullable;
import android.widget.ImageView;
import com.bumptech.glide.Glide;
import com.bumptech.glide.RequestManager;
import com.bumptech.glide.load.DataSource;
import com.bumptech.glide.load.engine.GlideException;
import com.bumptech.glide.request.RequestListener;
import com.bumptech.glide.request.target.Target;
import com.sergio.twitter.R;

import javax.inject.Inject;

public class ImageLoader {

    private final RequestManager requestManager;
    private Context context;

    @Inject
    public ImageLoader(Context context) {
        this.requestManager = Glide.with(context);
        this.context = context;
    }

    public void loadImage(String url, ImageView imageView, ImageLoaderListener listener) {
        requestManager.asBitmap().load(url).listener(new RequestListener<Bitmap>() {
            @Override
            public boolean onLoadFailed(@Nullable GlideException e, Object model, Target<Bitmap> target, boolean isFirstResource) {
                return false;
            }

            @Override
            public boolean onResourceReady(Bitmap resource, Object model, Target<Bitmap> target, DataSource dataSource, boolean isFirstResource) {
                if (listener != null) listener.onResourceReady(resource);
                return false;
            }
        }).into(imageView);
    }

    public interface ImageLoaderListener {

        void onResourceReady(Bitmap bitmap);
    }

}
